<p style="font-family:Arial, Helvetica, sans-serif;font-size:12px;background-color:#FFFFFF;">
	&nbsp; &nbsp; &nbsp; &nbsp; 为配合旅游法的实施，提升出境旅游服务质量，国家旅游局发布了《出境游优质服务供应商认定与测评标准》，通过实施「出境旅游优质服务供应商计划」，对境外接待中国游客的供应商进行QSC优质服务认证。这项认证计划为中国游客在境外旅行增添保障，也将帮助中方旅行社构建和优化境外供应商服务体系。QSC按照严格的评价体系，从接待服务能力、诚信经营、能够为中国游客提供中文服务和其他便利服务等各个方面，对境外地接旅行社、酒店、购物商店、旅游景点、餐馆等类型的供应商进行优质服务认证。
