	「出境旅游优质服务供应商计划」（简称QSC计划）及《出境游优质服务供应商认定与测评标准》发布会日前于北京举行。《出境游优质服务供应商认定与测评标准》是由国家旅游局发布实施的海外旅游服务质量认证标准，该项认证的实施和深入开展，将为中国游客在境外旅行增添有效保障。
